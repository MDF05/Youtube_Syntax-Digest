// 2 navigator.appName: Menunjukkan nama dari aplikasi browser yang sedang digunakan. Pada contoh di atas, nilai dari appName adalah "Netscape".
console.log(navigator.appName)

// 3 navigator.appVersion: Menunjukkan versi dari aplikasi browser yang sedang digunakan beserta informasi sistem operasi. Pada contoh di atas, nilai dari appVersion adalah "5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/112.0.0.0 Safari/537.36".
console.log(navigator.appVersion)

// 7 navigator.cookieEnabled: Menunjukkan apakah pengguna mengizinkan penggunaan cookie atau tidak.
console.log(navigator.cookieEnabled)


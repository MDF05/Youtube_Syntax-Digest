console.log(navigator.language)

// 17 navigator. languages: Menunjukkan daftar bahasa yang didukung oleh browser.
console.log(navigator.languages)

// 20 navigator.maxTouchPoints: Menunjukkan jumlah maksimum titik sentuhan yang didukung oleh perangkat yang digunakan.
